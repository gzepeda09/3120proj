
""" 
Create a Disease
mortality rate: 20%
lifetime of the virus: 2 days
duration of the infection: 1 week
transmission rate: 70%
"""

import random
import time
import os

os.system('clear')
while 1:
    num = random.randrange(1,20,1)
    if num < 2:
        print(num)
        break
    else:
        print(num)
    time.sleep(0.35)
    os.system('clear')
exit()

